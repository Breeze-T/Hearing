	/**
     * 将单元格全部设置为文本格式
     */
	public ExportExcel setCellFormat(ExportExcel excel,int Row,int Col) throws Exception {
		CellStyle cellStyle = excel.getWb().createCellStyle();
		DataFormat format = excel.getWb().createDataFormat();
		cellStyle.setDataFormat(format.getFormat("@"));
		Sheet sheet = excel.getSheet();
		Row row = null;
		for(int i= 1;i<Row;i++) {
			row = sheet.createRow(i);
			for(int j=0;j<Col;j++) {
				row.createCell(j).setCellStyle(cellStyle);;
			}
		}
		return excel;
	}
